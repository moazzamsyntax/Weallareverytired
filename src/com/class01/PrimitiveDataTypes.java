		/*
		 * byte - data type ---
		 * box1 - just a name of the variable - label of the box
		 * 10 - value that we assign to that variable
		 */ 
		
		//we are storing whole numbers values
		byte box1=10; //-128 till 127
		
		short box2=100; //-32768 to 32767
		
		int box3=1000; //-2147483648 to 2147483647
		
		long box4=100000000000000l;
		
		//we are storing floating numbers
		float f=12.99f;
		double d=12.95;
		
		//we are storing single characters values
		char a='*';
		char b='A';
		char c='1';
		
		//to store boolean values
		boolean variable1=true;
