package com.class03;

public class IfCondition {

	public static void main(String[] args) {

		int a = 10;
		int b = 70;

		if (a > b) {
			System.out.println("a is larger than b");
		} else {
			System.out.println("false");
		}

		int tem = 25;

		if (tem > 30) {
			System.out.println("I am going for a walk");
		} else {
			System.out.println("I'll stay at home");
		}
	}

}
